package view;

import java.awt.BorderLayout;
import java.awt.Dimension;
import java.awt.Font;

import javax.swing.JButton;
import javax.swing.JDialog;
import javax.swing.JPanel;
import javax.swing.JScrollPane;
import javax.swing.JTextArea;

public class HelpDialog extends JDialog {

    private static final String HELP_TEXT =
            """
            StageLayout Designer 使い方

            ■ 基本の流れ
            1. ファイル > 新規 で作業シートの大きさを選びます。
            2. イベント情報でイベント名、日付、会場、担当者、メモを入れます。
            3. 左の機材パレットから機材を選びます。
            4. キャンバスをダブルクリックして機材を置きます。
            5. 置いた機材はドラッグで移動できます。
            6. プレビューで提出用の見た目を確認します。
            7. 保存で作業データを残します。

            ■ よく使う操作
            ・機材パレットの機材を選び、キャンバスをダブルクリックすると配置できます。
            ・配置済みの機材は右クリックで編集、削除、表示名変更ができます。
            ・選択中の機材や文字は、右下ハンドルでサイズ変更できます。
            ・選択した機材はドラッグで移動できます。
            ・選択中の機材は、上の丸いハンドルをドラッグして向きを変えられます。
            ・Shiftを押しながら回すと15度刻みになります。
            ・Rキー、または回転ボタンでも向きを変えられます。
            ・+ / - キーで大きさを変えられます。
            ・Deleteキーで選択中の機材を消せます。
            ・Ctrl+Zで戻る、Ctrl+Yでやり直しができます。
            ・ツールバーの保存ボタンで今のファイルを保存できます。
            ・グリッドはマス目の表示、吸着は配置をマス目に合わせる設定です。
            ・整列ガイドをONにすると、機材を動かす時にほかの機材の中心線や端へ合わせやすくなります。
            ・配置済み機材を右クリックすると、名前変更、コピー、前面/背面、削除ができます。
            ・文字ボックス、会場パーツ、背景図面も右クリックからよく使う操作を選べます。

            ■ 線・バミリ線
            「線を引く」では、クリックした点から次にクリックした点へ線を引けます。
            「バミリ線」では、赤いテープ用の線を引けます。バミリ線にはメートル表示は出ません。
            線の種類は、通常線、ケーブル線、導線から選べます。
            ケーブル線は黒い実線、導線は青い矢印付きの線で表示されます。
            Shiftを押しながら引くと、縦か横にそろえやすくなります。
            Ctrlを押しながら引くと、1m単位に合わせやすくなります。

            線を消す時は、線をクリックして選択してからDeleteキー、または右クリックの削除を使います。
            線を引いている途中でも、既にある線を右クリックすれば削除できます。
            選択中の線は、両端の赤い点をドラッグして位置を直せます。

            ■ 背景図面
            ホールや会場のフロアマップ画像を背景図面として読み込めます。
            背景図面は下絵として表示され、その上に機材、人物、線、テキストを配置できます。
            PNG/JPG/JPEG/PDF形式に対応しています。
            PDFは先頭ページを背景図面として読み込みます。
            読み込み時に実寸幅を入れると、グリッドに合わせた大きさで配置できます。

            背景図面を固定すると、誤って移動しないようになります。
            機材や線を配置するときは、背景図面を固定しておくと作業しやすくなります。
            テンプレートメニューまたは背景図面メニューから、読み込んだPDFや画像をローカルテンプレートとして登録できます。

            ■ テキストボックス
            文字モードを使うと、図面上に任意の文字を配置できます。
            受付、音響卓、出演者待機、立入禁止などの補足説明に利用できます。
            配置した文字はドラッグで移動できます。
            文字をダブルクリックすると内容を編集できます。
            未選択の時は文字だけ表示されます。
            マウスを乗せると薄い枠、選択すると枠と右下ハンドルが表示されます。
            提出用プレビューでは、基本的に文字だけ表示されます。

            ■ 機材パレットとモード
            線描画モードや文字モード中でも、機材パレットから機材を選ぶと自動で機材配置モードに戻ります。

            ■ ラベル表示
            右側の詳細パネルで、選択中の機材やバミリのラベル表示を切り替えられます。
            全体の名前表示がONで、個別のラベル表示もONの時だけラベルが表示されます。

            ■ 会場テンプレート
            会場は最初から特別な編集モードに切り替えなくても作れます。
            左のパレットの 舞台 > 会場パーツ から四角形や円形を置いて会場を作ります。
            テンプレート > 図形テンプレートを作成 から、長方形、半円前面、楕円形、陸上トラック風などをサイズ入力で作成できます。
            作成した図形は、通常の会場パーツと同じように移動、サイズ変更、削除、保存できます。

            会場ができたら ファイル > 会場だけ保存 で保存します。
            背景図面を読み込んでいる場合は、背景も会場テンプレートとして保存されます。
            保存した会場は ファイル > 会場テンプレートを読み込み で使えます。

            会場固定をONにすると、会場パーツを動かしにくくできます。
            当日の仕込み図では、会場を固定して、その上に機材を置くイメージです。

            ■ マイリスト
            よく使う機材は、機材ボタンを右クリックしてマイリストに追加できます。
            マイリストはこのアプリ全体で共通です。
            照明や音響の種類が多い時は、よく使うものだけをマイリストに入れると探しやすくなります。

            ■ プレビュー
            プレビューでは、提出用の配置図、必要機材一覧、メモをまとめて確認できます。
            プレビュー範囲は、ステージ範囲、会場テンプレート、配置物、背景図面、作業シート全体から選べます。
            会場全体PDFで余白が多い時は、会場テンプレートに合わせるか配置物に合わせるを選ぶと見やすくなります。
            PNG画像やPDFとして保存することも、印刷することもできます。

            ■ 困った時
            ・作業場所が広すぎる時は、表示倍率を「全体」にします。
            ・置いたものが動かない時は、会場固定がONになっていないか確認します。
            ・会場を作る時は、まず大きめの四角形や線でざっくり作ってから細かく整えると楽です。
            v1.1.1 補足
            ・プレビューは、編集画面の名前表示設定に合わせて表示します。
            ・画像付き機材は、プレビューで余計な黒枠を出しません。
            ・文字ボックスは、ダブルクリックで本文、サイズ、背景、枠線を編集できます。
            ・文字ボックスは、未選択時とプレビューでは文字だけを表示します。
            ・文字モードでは、キャンバスをダブルクリックして文字ボックスを作成します。
            ・文字ボックスは、選択後に右下ハンドルでもサイズ変更できます。
            ・バミリ線にはメートル表示を出しません。
            ・バミリは、通常の機材と同じようにダブルクリックで配置します。
            ・バミリは横、縦、X、十字、L、T、自由線を選べます。
            ・バミリ自由線だけは、クリックした点をつないで描画します。
            ・PDF背景は、PDFの先頭ページを背景図面として読み込みます。
            ・背景図面メニューから、シート幅、シート高さ、実寸幅に合わせ直せます。
            ・背景図面メニューから、PDFや画像背景をテンプレート登録できます。
            ・線を消す時は、線を選択してDelete、または既存の線を右クリックします。
            """;

    public HelpDialog(MainFrame owner) {

        super(owner, "使い方", true);

        setLayout(new BorderLayout(8, 8));
        setSize(new Dimension(680, 620));
        setLocationRelativeTo(owner);

        JTextArea textArea = new JTextArea(HELP_TEXT);
        textArea.setEditable(false);
        textArea.setLineWrap(true);
        textArea.setWrapStyleWord(true);
        textArea.setFont(new Font("SansSerif", Font.PLAIN, 14));
        textArea.setCaretPosition(0);

        add(new JScrollPane(textArea), BorderLayout.CENTER);

        JPanel buttonPanel = new JPanel();
        JButton closeButton = new JButton("閉じる");
        closeButton.addActionListener(e -> dispose());
        buttonPanel.add(closeButton);

        add(buttonPanel, BorderLayout.SOUTH);
    }
}
